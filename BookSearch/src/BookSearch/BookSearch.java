    private void tipoPesquisaInicianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tipoPesquisaInicianteActionPerformed
        // TODO add your handling code here:
        myUsuario.setTipoLeitor(TipoDeLeitor.Simples); 
        myUsuario.setNomeLeitor(nomeUsuarioResp1.getText()); 
        
        String valorIdade = idadeComboBox.getSelectedItem().toString();
        if(valorIdade.contains("-")) {
            valorIdade = "-1";
        }
        myUsuario.setIdadeLeitor(Integer.parseInt(valorIdade));  // Pegando a idade e convertendo para Int
        
        if(myUsuario.getNomeLeitor().length() > 0 && myUsuario.getIdadeLeitor() >= 0)
        {
            trocarTela(generoChoiceFrame, this, 640, 500);
        } 
            
        else 
        {
            mensagemErro.setText("É necessário preencher as informações!");
        }
    }//GEN-LAST:event_tipoPesquisaInicianteActionPerformed

