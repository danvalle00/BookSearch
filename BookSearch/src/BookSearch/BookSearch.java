    private void mostrarLivro()
    {
        outputInfoUsuario.setText(myUsuario.toString());
        try
        {
            outputLivro1.setText(myPesquisa.recomendarLivro().get(0).toString()); // oque vai ser (Livro 1)
            outputLivro2.setText(myPesquisa.recomendarLivro().get(1).toString());// (Livro 2)
            outputLivro3.setText(myPesquisa.recomendarLivro().get(2).toString()); // livro 3 
            outputLivro4.setText(myPesquisa.recomendarLivro().get(3).toString()); // livro 4
            outputLivro5.setText(myPesquisa.recomendarLivro().get(4).toString()); // livro 5
        }
        catch(IndexOutOfBoundsException exc)
        {
            System.out.println("Out of bounds: Livros não encontrados");
        }
            
    }
        
 
